FString UMcpBlueprintGraphPinLibrary::GetPinTypeAsJsonSchema(const FMcpBlueprintGraphPin& Pin)
{
	UEdGraphPin* NativePin = GetNativePinSafe(Pin);
	if(!NativePin)
	{
		return TEXT("");
	}

	UK2Node* OwningNode = Cast<UK2Node>(NativePin->GetOwningNode());
	if(!OwningNode)
	{
		return TEXT("");
	}

	const FEdGraphPinType& PinType = NativePin->PinType;
	if (PinType.PinCategory == UEdGraphSchema_K2::PC_Exec)
	{
		return TEXT("{\"type\":\"exec\"}");
	}

	if (PinType.PinCategory == UEdGraphSchema_K2::PC_Boolean)
	{
		return TEXT("{\"type\":\"boolean\"}");
	}

	if (PinType.PinCategory == UEdGraphSchema_K2::PC_Int || PinType.PinCategory == UEdGraphSchema_K2::PC_Int64)
	{
		return TEXT("{\"type\":\"integer\"}");
	}

	if (PinType.PinCategory == UEdGraphSchema_K2::PC_Real || PinType.PinCategory == UEdGraphSchema_K2::PC_Float || PinType.PinCategory == UEdGraphSchema_K2::PC_Double)
	{
		return TEXT("{\"type\":\"number\"}");
	}

	if (PinType.PinCategory == UEdGraphSchema_K2::PC_String || PinType.PinCategory == UEdGraphSchema_K2::PC_Text || PinType.PinCategory == UEdGraphSchema_K2::PC_Name)
	{
		return TEXT("{\"type\":\"string\"}");
	}

	return FString::Printf(TEXT("{\"type\":\"object\",\"pinCategory\":\"%s\"}"), *PinType.PinCategory.ToString());
}

