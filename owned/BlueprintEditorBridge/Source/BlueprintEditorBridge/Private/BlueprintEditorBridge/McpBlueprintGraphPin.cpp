TArray<FMcpBlueprintGraphPin> UMcpBlueprintGraphPinLibrary::ListConnectedPins(const FMcpBlueprintGraphPin& Pin)
{
	TArray<FMcpBlueprintGraphPin> Result;
	UEdGraphPin* NativePin = GetNativePinSafe(Pin);
	if(!NativePin)
	{
		return Result;
	}

	for(UEdGraphPin* LinkedToPin : NativePin->LinkedTo)
	{
		FMcpBlueprintGraphPin WrappedPin = FromNativePin(LinkedToPin);
		if(::IsValid(WrappedPin.Node))
		{
			Result.Add(WrappedPin);
		}
	}
	return Result;
}

UEdGraphPin* UMcpBlueprintGraphPinLibrary::GetNativePinSafe(const FMcpBlueprintGraphPin& Pin)
{
	if(!::IsValid(Pin.Node))
	{
		return nullptr;
	}

	// Collect visible pins in the requested direction so we can index into them
	// and perform an outward search if needed.
	TArray<UEdGraphPin*, TInlineAllocator<16>> DirectionalPins;
	for(UEdGraphPin* NativePin : Pin.Node->Pins)
	{
		if(!NativePin->bHidden && NativePin->Direction == Pin.PinDirection)
		{
			DirectionalPins.Add(NativePin);
		}
	}

	// Fast path: index and name both match
	if(DirectionalPins.IsValidIndex(Pin.PinIndex) && DirectionalPins[Pin.PinIndex]->PinName == Pin.PinName)
	{
		return DirectionalPins[Pin.PinIndex];
	}

	// The pin at the expected index has a different name — the graph has been
	// modified and indices have drifted. Search outward from PinIndex to find
	// the nearest pin that still has the correct name.
	for(int32 Distance = 1; Distance < DirectionalPins.Num(); ++Distance)
	{
		const int32 Forward = Pin.PinIndex + Distance;
		if(DirectionalPins.IsValidIndex(Forward) && DirectionalPins[Forward]->PinName == Pin.PinName)
		{
			return DirectionalPins[Forward];
		}

		const int32 Backward = Pin.PinIndex - Distance;
		if(DirectionalPins.IsValidIndex(Backward) && DirectionalPins[Backward]->PinName == Pin.PinName)
		{
			return DirectionalPins[Backward];
		}
	}

	return nullptr;
}
